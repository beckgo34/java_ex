package memo;

public class While {
    /**
     * while문
     * 횟수가 정해져 있지 않은 상황에서 활용을 할 수 있음
     * for문과 동일한 목적으로도 사요 ㅇ가능
     *
     * *while, do while
     *   while: 반복조건을 만족해야 한 번 이라도 실행
     *   do while: 반복조건 만족과 상관없이 한 번은 실행하고 조건을 따짐
     *
     *   문법
     *
     *   while ([조건식]){
     *       조건을 만족할 경우 실행할 문장
     *   }
     *
     *      do{
     *          조건을 따지기 전에 한 번은 실행됨
     *          조건을 만족하면 반복 실행
     *
     *      }while([조건식]); //마지막에 세미콜론이 붙음
     */
}
